'use strict';

/* eslint-disable no-console */
//
// AI-search retrieval evaluation harness.
//
// PURPOSE — a stable measuring instrument for the prompt/retrieval improvement
// loop. It exercises the REAL production code (electron/db.js + electron/analyzer.js)
// against a read-only COPY of the live archive and scores three pillars the user
// cares about: TAGS, KEYWORDS, SEARCH RESULTS.
//
// RUNTIME — must run under Electron's node (better-sqlite3 is built for Electron's
// ABI, not the system node). The npm script wires this up:
//     npm run eval:search            # deterministic loop (fast, no LLM)
//     npm run eval:search -- --llm   # also run the real model end-to-end
//     npm run eval:search -- --case=cuffie
//
// ISOLATION — the harness copies shelfy.sqlite (+ -wal/-shm) into a scratch
// userData dir and points the production code there via an `electron` shim, so it
// NEVER mutates the real archive. Model weights are symlinked for --llm mode.
//
// GROUND TRUTH — gold post/tag sets are computed from the DB via an INDEPENDENT
// read-only connection (raw SQL), so scoring can't be gamed by the code under test.
//
// DO NOT EDIT THIS FILE during the improvement loop. Iterate on the production
// retrieval/prompt code instead; this is the contract that measures it.

const fs = require('fs');
const os = require('os');
const path = require('path');
const { orderMetrics } = require('./order-metrics.cjs');   // P9
const { summarizeRuns } = require('../lib/agg-stats.cjs');  // P11

// ── 0. Runtime guard ────────────────────────────────────────────────────────
if (!process.versions.electron) {
  console.error('\n[eval] This harness must run under Electron node (better-sqlite3 ABI).');
  console.error('[eval] Use:  npm run eval:search   (not plain `node`).\n');
  process.exit(2);
}

// ── 1. Args ───────────────────────────────────────────────────────────────────
const args = process.argv.slice(2);
const WITH_LLM = args.includes('--llm');
const caseArg = (args.find((a) => a.startsWith('--case=')) || '').split('=')[1] || null;
// P11: numero di esecuzioni per caso. Default 3. Con K>1 ogni caso è valutato K
// volte e il report riporta mediana + dispersione (varianza/IQR) per metrica.
// Per la pipeline deterministica (senza --llm) i K run sono identici, ma il flag
// resta utile per --llm (non deterministico) e per validare l'aggregazione.
const RUNS = Math.max(1, Number((args.find((a) => a.startsWith('--runs=')) || '').split('=')[1]) || 3);

// ── 2. Scratch userData (read-only snapshot of the real archive) ───────────────
const REAL_USERDATA = path.join(os.homedir(), 'Library', 'Application Support', 'Shelfy');
const SCRATCH = path.join(__dirname, '.scratch');
const SCRATCH_DB = path.join(SCRATCH, 'shelfy.sqlite');

function setupScratch() {
  fs.mkdirSync(SCRATCH, { recursive: true });
  // Fresh snapshot every run: copy the DB and its WAL/SHM sidecars so we read a
  // consistent view even if the app left uncommitted WAL frames.
  for (const suffix of ['', '-wal', '-shm']) {
    const src = path.join(REAL_USERDATA, `shelfy.sqlite${suffix}`);
    const dst = `${SCRATCH_DB}${suffix}`;
    if (fs.existsSync(src)) fs.copyFileSync(src, dst);
    else if (fs.existsSync(dst)) fs.rmSync(dst);
  }
  // Force a model whose weights actually exist, for --llm mode.
  fs.writeFileSync(path.join(SCRATCH, 'ai-model.json'), JSON.stringify({ modelId: 'qwen3vl-4b' }));
  // Symlink the real model weights so analyzer can spawn llama-server in --llm mode.
  const realModels = path.join(REAL_USERDATA, 'models');
  const linkModels = path.join(SCRATCH, 'models');
  try {
    if (!fs.existsSync(linkModels) && fs.existsSync(realModels)) {
      fs.symlinkSync(realModels, linkModels, 'dir');
    }
  } catch (e) {
    if (WITH_LLM) console.warn('[eval] could not link models dir:', e.message);
  }
}

// ── 3. electron shim — make the production modules look at the scratch dir ──────
function installElectronShim() {
  const Module = require('module');
  const orig = Module._load;
  const fake = {
    app: {
      getPath: (k) => (k === 'userData' ? SCRATCH : os.tmpdir()),
      getName: () => 'ShelfyEval',
      getVersion: () => '0.0.0',
      isPackaged: false,
    },
  };
  Module._load = function (request, ...rest) {
    if (request === 'electron') return fake;
    return orig.call(this, request, ...rest);
  };
}

// ── 4. Small helpers ────────────────────────────────────────────────────────
const lc = (s) => String(s ?? '').trim().toLowerCase();
const uniq = (a) => [...new Set(a)];
function pct(x) { return x == null ? '  — ' : `${(x * 100).toFixed(0).padStart(3)}%`; }
function clamp01(x) { return Math.max(0, Math.min(1, x)); }
function jaccardHits(list, set) { return list.filter((t) => set.has(lc(t))).length; }
// Rinomina le chiavi di un pacchetto di metriche con un prefisso (per non far
// collidere le metriche d'ordine della ricerca hybrid/solo-tag con quelle del
// testo puro nello stesso oggetto `metrics`).
function prefixKeys(obj, pfx) {
  const out = {};
  for (const [k, v] of Object.entries(obj || {})) out[`${pfx}${k}`] = v;
  return out;
}

// ── 5. Ground truth (independent read-only connection) ─────────────────────────
function makeOracle() {
  const Database = require('better-sqlite3');
  const gdb = new Database(SCRATCH_DB, { readonly: true, fileMustExist: true });

  const globalTagCount = new Map();
  for (const r of gdb.prepare('SELECT tag_norm AS t, COUNT(*) c FROM post_tags GROUP BY tag_norm').all()) {
    globalTagCount.set(r.t, r.c);
  }

  // Posts whose text/ai_description/ai_keywords/ai_tags mention any gold term.
  function goldPosts(terms) {
    const cols = ['text', 'ai_description', 'ai_keywords', 'ai_tags'];
    const where = terms.map(() => `(${cols.map((c) => `${c} LIKE ?`).join(' OR ')})`).join(' OR ');
    const params = terms.flatMap((t) => cols.map(() => `%${t}%`));
    const ids = gdb.prepare(`SELECT id FROM posts WHERE ${where}`).all(...params).map((r) => r.id);
    return new Set(ids);
  }

  // tag_norm → how many of the gold posts carry it (topic association strength).
  function goldTagCounts(idSet) {
    const m = new Map();
    if (!idSet.size) return m;
    const ids = [...idSet];
    const ph = ids.map(() => '?').join(',');
    for (const r of gdb
      .prepare(`SELECT tag_norm AS t, COUNT(*) c FROM post_tags WHERE post_id IN (${ph}) GROUP BY tag_norm`)
      .all(...ids)) {
      m.set(r.t, r.c);
    }
    return m;
  }

  // IDEAL tags for a query (display hint): tags carried by the gold posts, ranked
  // by a blend of prevalence (in_set) and distinctiveness (lift). Prefer tags with
  // real mass (in_set ≥ 2); only if none qualify, fall back to distinctive 1-offs.
  function goldTags(goldCounts, { top = 12 } = {}) {
    const all = [...goldCounts.entries()]
      .map(([tag, in_set]) => ({ tag, in_set, lift: in_set / (globalTagCount.get(tag) || in_set) }));
    const rank = (rows) => rows
      .sort((a, b) => b.in_set * b.lift - a.in_set * a.lift)
      .slice(0, top)
      .map((r) => r.tag);
    const main = all.filter((r) => r.in_set >= 2 && r.lift >= 0.03);
    return rank(main.length ? main : all.filter((r) => r.lift >= 0.25));
  }

  // ── P12: gold ALTERNATIVO non circolare ────────────────────────────────────
  // Il gold di default (goldPosts) deriva da text/ai_description/ai_keywords/
  // ai_tags, cioè dagli STESSI campi che la pipeline usa per il retrieval →
  // misura circolare (vedi README.md "Caveat sulla circolarità"). Se un caso
  // fornisce `humanGold` (lista di post id annotati a mano), QUELLA lista è
  // l'insieme rilevante e ha precedenza: nessun campo ai_* viene consultato per
  // derivare il set. I conteggi tag/lift restano calcolati sui post del set
  // (qualunque ne sia l'origine), così le metriche TAG continuano a funzionare.
  function humanGoldPosts(ids) {
    // Tieni solo gli id realmente presenti nella copia del DB, così il conteggio
    // gold non è gonfiato da annotazioni che puntano a post rimossi.
    if (!ids || !ids.length) return new Set();
    const want = ids.map((x) => String(x));
    const ph = want.map(() => '?').join(',');
    const present = gdb.prepare(`SELECT id FROM posts WHERE id IN (${ph})`).all(...want).map((r) => r.id);
    return new Set(present);
  }

  return { gdb, globalTagCount, goldPosts, goldTagCounts, goldTags, humanGoldPosts };
}

// ── 6. Pipeline under test (mirror chatSearch's candidate construction) ─────────
const BROAD_LIMIT = 150;  // matches analyzer BROAD_VOCAB_LIMIT
const RESULT_LIMIT = 60;

function runPipeline(db, analyzer, query) {
  const broadVocab = (db.getTagStats({ limit: BROAD_LIMIT }) || []).map((t) => t.tag).filter(Boolean);
  const broadSet = new Set(broadVocab.map(lc));
  const excludeSet = new Set(broadSet);

  let specific = [];
  try { specific = analyzer.retrieveSpecificTags(query, excludeSet) || []; } catch (e) { specific = [`<ERR:${e.message}>`]; }

  // Keyword retrieval is part of the desired behavior; may not exist yet.
  let keywords = [];
  if (typeof analyzer.retrieveKeywords === 'function') {
    try { keywords = analyzer.retrieveKeywords(query, { limit: 12 }) || []; } catch (e) { keywords = [`<ERR:${e.message}>`]; }
  }

  // Pure free-text search path (what the gallery falls back to with no tags) —
  // this is where the stopword-pollution bug shows up directly.
  let textSearch = { posts: [], total: 0 };
  try { textSearch = db.searchPostsHybrid([], query, { limit: RESULT_LIMIT }) || textSearch; } catch (e) { /* noop */ }

  return { broadVocab, broadSet, specific, keywords, textSearch };
}

// ── 7. Scoring ────────────────────────────────────────────────────────────────
function scoreCase(c, oracle, pipe, db) {
  // P12: se il caso porta un gold umano (post id annotati a mano), ha PRECEDENZA
  // sul gold derivato dai campi ai_* (circolare). goldSource documenta quale è
  // stato usato, così il report è onesto su cosa sta misurando.
  const hasHuman = Array.isArray(c.humanGold) && c.humanGold.length > 0;
  const gold = hasHuman ? oracle.humanGoldPosts(c.humanGold) : oracle.goldPosts(c.goldTerms);
  const goldSource = hasHuman ? 'human' : 'ai-derived';
  const goldCounts = oracle.goldTagCounts(gold);   // tag_norm → # gold posts carrying it
  const goldTags = oracle.goldTags(goldCounts, { top: 12 });

  // ── P1/P8 PROBE: ricerca hybrid (tag+testo) e solo-tag ─────────────────────
  // Il path solo-testo (pipe.textSearch, tag=[]) NON esercita né la fusione
  // tag+testo (P1) né il ranking IDF dei tag (P8). Qui sondiamo proprio quei path
  // usando come filtro i goldTags — tag REALI portati dai post rilevanti: se
  // l'utente filtrasse per i tag più pertinenti, aggiungere la query testuale
  // migliora l'ordine (P1)? e la ricerca solo-tag mette i gold in cima (P8)? Sono
  // metriche DIAGNOSTICHE: non entrano nello score/threshold composito (che resta
  // testuale), ma rendono P1/P8 finalmente misurabili e confrontabili in A/B.
  // hybrid: pochi tag forti (top-2) + query testuale → misura la FUSIONE (P1).
  // tag-only: più tag (top-5, frequenze diverse) → dà all'IDF di P8 modo di
  // riordinare i post che matchano sottoinsiemi diversi dei tag.
  const probeTags = (goldTags || []).slice(0, 2);
  // tagProbeOverride (caso ad hoc P8): forza i tag della sonda solo-tag, così si
  // può costruire deliberatamente uno scenario raro+comune che l'IDF riordina.
  const tagProbe = Array.isArray(c.tagProbeOverride) && c.tagProbeOverride.length
    ? c.tagProbeOverride.map(lc)
    : (goldTags || []).slice(0, 5);
  let hybridOrder = {};
  let tagOnlyOrder = {};
  if (db && probeTags.length) {
    try {
      const hy = db.searchPostsHybrid(probeTags, c.query, { limit: RESULT_LIMIT }) || { posts: [] };
      hybridOrder = orderMetrics(hy.posts.map((p) => p.id), gold);
    } catch { /* path non disponibile in baseline */ }
  }
  if (db && tagProbe.length) {
    try {
      const to = db.searchPostsByTags(tagProbe, { limit: RESULT_LIMIT }) || { posts: [] };
      tagOnlyOrder = orderMetrics(to.posts.map((p) => p.id), gold);
    } catch { /* path non disponibile */ }
  }

  const specificLc = pipe.specific.map(lc);
  // A pool tag is ON-TOPIC if it's distinctive of the relevant set — i.e. a
  // meaningful fraction of its archive occurrences land on gold posts (lift ≥
  // 0.15). This credits rare-but-relevant tags (product design, 2/2) while
  // rejecting frequent-but-incidental ones (performance, 1/many). NOISE = a tag
  // carried by ZERO relevant posts. Broad pool is excluded: it's the same top-150
  // for every query, so it can't discriminate.
  const lift = (t) => (goldCounts.get(lc(t)) || 0) / (oracle.globalTagCount.get(lc(t)) || 1);
  const onTopic = (t) => (goldCounts.get(lc(t)) || 0) >= 1 && lift(t) >= 0.15;
  const poolRelevance = specificLc.length ? specificLc.filter(onTopic).length / specificLc.length : 0;
  const poolNoise = specificLc.length
    ? specificLc.filter((t) => (goldCounts.get(t) || 0) === 0).length / specificLc.length
    : 0;
  const rejectHits = specificLc.filter((t) => (c.rejectTags || []).map(lc).includes(t));

  // ── KEYWORDS ── fraction of suggested keywords that touch a gold term.
  const kwLc = pipe.keywords.map(lc);
  const keywordRelevance = kwLc.length
    ? kwLc.filter((k) => c.goldTerms.some((g) => k.includes(lc(g)) || lc(g).includes(k))).length / kwLc.length
    : 0;

  // ── SEARCH RESULTS ── the gallery shows the result page ordered by recency, so
  // first-page precision/recall is the real UX. searchTotal exposes stopword bloat
  // (a query that matches ~the whole archive is the core defect).
  const returned = pipe.textSearch.posts.map((p) => p.id);
  const hit = returned.filter((id) => gold.has(id)).length;
  const searchRecall = gold.size ? hit / Math.min(gold.size, RESULT_LIMIT) : null;
  const searchPrecision = returned.length ? hit / returned.length : null;

  // ── P9 METRICHE D'ORDINE ── precision/recall set-based ignorano la POSIZIONE.
  // `returned` è l'ordine reale restituito dalla pipeline (la prima pagina della
  // gallery), quindi p@k/MRR/nDCG misurano se i gold sono in CIMA, non solo
  // presenti. Affiancano (non sostituiscono) le metriche set-based qui sopra.
  const order = orderMetrics(returned, gold);

  const parts = [
    [0.30, poolRelevance],
    [0.15, keywordRelevance],
    [0.25, searchPrecision],
    [0.30, searchRecall],
  ].filter(([, v]) => v != null);
  const wsum = parts.reduce((s, [w]) => s + w, 0);
  let score = parts.reduce((s, [w, v]) => s + w * v, 0) / (wsum || 1);
  if (rejectHits.length) score -= 0.5;
  score = clamp01(score);

  const threshold = c.kind === 'hard' ? 0.45 : 0.55;
  const pass = score >= threshold && rejectHits.length === 0;

  return {
    id: c.id, kind: c.kind, query: c.query,
    goldPostCount: gold.size, goldTags, goldSource,
    // metrics: set-based (esistenti) + d'ordine (P9), affiancate nello stesso oggetto.
    // metrics: set-based + d'ordine testo (P9) + sonde hybrid/solo-tag (P1/P8).
    metrics: {
      poolRelevance, poolNoise, keywordRelevance, searchPrecision, searchRecall, ...order,
      ...prefixKeys(hybridOrder, 'hy_'), ...prefixKeys(tagOnlyOrder, 'tag_'),
    },
    rejectHits, score, threshold, pass,
    sample: {
      specificHead: pipe.specific.slice(0, 14),
      keywords: pipe.keywords.slice(0, 12),
      searchTotal: pipe.textSearch.total,
      searchHit: hit,
      probeTags,
    },
  };
}

// ── 8. Optional: real LLM end-to-end ───────────────────────────────────────────
async function runLlm(analyzer, c) {
  const status = analyzer.getModelStatus();
  if (!status.ready) return { ready: false, modelId: status.modelId };
  const res = await analyzer.chatSearch([{ role: 'user', content: c.query }], [], null, null);
  return {
    ready: true,
    modelId: status.modelId,
    reply: (res.reply || '').slice(0, 240),
    broad: res.tagGroups?.broad || [],
    specific: res.tagGroups?.specific || [],
    keywords: res.tagGroups?.keywords || res.keywords || [],
  };
}

// ── 9. Report ─────────────────────────────────────────────────────────────────
function printReport(results, llmResults) {
  console.log('\n══════════════════════════════════════════════════════════════════');
  console.log('  AI-SEARCH RETRIEVAL EVAL   (pillars: TAGS · KEYWORDS · RESULTS)');
  console.log('══════════════════════════════════════════════════════════════════');
  for (const r of results) {
    const m = r.metrics;
    console.log(`\n▌ ${r.id}  [${r.kind}]  ${r.pass ? '✅ PASS' : '❌ FAIL'}  score=${r.score.toFixed(2)} (≥${r.threshold})`);
    console.log(`  query: "${r.query}"`);
    console.log(`  gold: ${r.goldPostCount} posts [${r.goldSource}] | ideal tags: ${r.goldTags.slice(0, 8).join(', ') || '(none)'}`);
    console.log(`  TAGS    poolRelevance=${pct(m.poolRelevance)}  poolNoise=${pct(m.poolNoise)}` +
                (r.rejectHits.length ? `  ⚠ REJECT: ${r.rejectHits.join(', ')}` : ''));
    console.log(`  KEYWORD relevance=${pct(m.keywordRelevance)}`);
    console.log(`  RESULTS precision=${pct(m.searchPrecision)}  recall=${pct(m.searchRecall)}  (hit ${r.sample.searchHit}/${r.goldPostCount}, total returned ${r.sample.searchTotal})`);
    // P9: metriche d'ordine (posizione dei gold nella lista restituita).
    console.log(`  ORDER   p@5=${pct(m['p@5'])} p@10=${pct(m['p@10'])} r@5=${pct(m['r@5'])} r@10=${pct(m['r@10'])} MRR=${m.mrr == null ? ' — ' : m.mrr.toFixed(2)} nDCG@10=${pct(m['ndcg@10'])}`);
    // P1/P8: sonde su ricerca tag+testo (hybrid) e solo-tag, sui goldTags reali.
    const mrrf = (v) => (v == null ? ' — ' : v.toFixed(2));
    console.log(`  HYBRID  (tag+testo, P1)  p@10=${pct(m['hy_p@10'])} MRR=${mrrf(m['hy_mrr'])} nDCG@10=${pct(m['hy_ndcg@10'])}` +
                `   |  TAG-ONLY (P8) p@10=${pct(m['tag_p@10'])} MRR=${mrrf(m['tag_mrr'])} nDCG@10=${pct(m['tag_ndcg@10'])}`);
    console.log(`  · probe tags   : ${(r.sample.probeTags || []).join(', ') || '(none)'}`);
    console.log(`  · specific pool: ${r.sample.specificHead.join(', ') || '(empty)'}`);
    console.log(`  · keywords     : ${r.sample.keywords.join(', ') || '(none)'}`);
    const llm = (llmResults || {})[r.id];
    if (llm) {
      if (!llm.ready) console.log(`  · LLM: model "${llm.modelId}" not ready — skipped`);
      else {
        console.log(`  · LLM(${llm.modelId}) broad   : ${llm.broad.join(', ') || '(empty)'}`);
        console.log(`  · LLM(${llm.modelId}) specific: ${llm.specific.join(', ') || '(empty)'}`);
        if (llm.keywords?.length) console.log(`  · LLM keywords: ${llm.keywords.join(', ')}`);
      }
    }
  }
  const passed = results.filter((r) => r.pass).length;
  console.log('\n──────────────────────────────────────────────────────────────────');
  console.log(`  OVERALL: ${passed}/${results.length} passed   →  ${passed === results.length ? '✅ ALL PASS' : '❌ NOT YET'}`);
  console.log('──────────────────────────────────────────────────────────────────\n');
  return passed === results.length;
}

// ── 10. Main ────────────────────────────────────────────────────────────────
async function main() {
  setupScratch();
  installElectronShim();

  const db = require('../../electron/db');
  const analyzer = require('../../electron/analyzer');
  db.initialize();

  const oracle = makeOracle();
  let cases = require('./cases.cjs');
  if (caseArg) cases = cases.filter((c) => c.id === caseArg);
  if (!cases.length) { console.error(`[eval] no case matches --case=${caseArg}`); process.exit(2); }

  // ── P11: K run per caso ─────────────────────────────────────────────────────
  // Eseguiamo ogni caso RUNS volte. `results` (per il report leggibile) usa
  // l'ultimo run di ogni caso; `runsAgg` riassume mediana/varianza/IQR di ogni
  // metrica attraverso i RUNS run. Per la pipeline deterministica i run coincidono
  // (varianza ≈ 0): l'aggregazione conta soprattutto con --llm o pipeline future
  // non deterministiche, ma il flag è già operativo qui.
  const perCaseRuns = new Map(cases.map((c) => [c.id, []]));
  let results = [];
  for (let run = 0; run < RUNS; run++) {
    results = cases.map((c) => {
      const r = scoreCase(c, oracle, runPipeline(db, analyzer, c.query), db);
      // Includiamo `score` (composito) accanto alle metriche per aggregarlo anch'esso.
      perCaseRuns.get(c.id).push({ ...r.metrics, score: r.score });
      return r;
    });
  }
  // Aggregazione per caso: { id → { metrica → {median,variance,iqr,...} } }.
  const runsAgg = cases.map((c) => ({
    id: c.id,
    runs: RUNS,
    metrics: summarizeRuns(perCaseRuns.get(c.id)),
  }));

  let llmResults = null;
  if (WITH_LLM) {
    llmResults = {};
    for (const c of cases) {
      process.stderr.write(`[eval] LLM run: ${c.id} …\n`);
      try { llmResults[c.id] = await runLlm(analyzer, c); }
      catch (e) { llmResults[c.id] = { ready: true, error: e.message, broad: [], specific: [] }; }
    }
    try { analyzer.shutdown(); } catch { /* noop */ }
  }

  const allPass = printReport(results, llmResults);

  // P11: riepilogo dispersione in console (mediana ± IQR di score e nDCG@10).
  if (RUNS > 1) {
    console.log(`  P11: ${RUNS} run/caso — mediana[IQR] score / nDCG@10:`);
    for (const a of runsAgg) {
      const sc = a.metrics.score || {};
      const nd = a.metrics['ndcg@10'] || {};
      const f = (x) => (x == null ? ' — ' : x.toFixed(2));
      console.log(`    ${a.id.padEnd(12)} score ${f(sc.median)}[IQR ${f(sc.iqr)}]  nDCG@10 ${f(nd.median)}[IQR ${f(nd.iqr)}]`);
    }
    console.log('──────────────────────────────────────────────────────────────────\n');
  }

  const report = { ts: new Date().toISOString(), withLlm: WITH_LLM, runs: RUNS, allPass, results, runsAgg, llmResults };
  fs.writeFileSync(path.join(__dirname, 'last-report.json'), JSON.stringify(report, null, 2));

  try { oracle.gdb.close(); } catch { /* noop */ }
  process.exit(allPass ? 0 : 1);
}

main().catch((e) => { console.error('[eval] fatal:', e); process.exit(3); });
